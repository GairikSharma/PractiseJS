var given_number = 99;
switch (given_number) {
    case 10:
        {
            console.log(given_number + 1);
            break;
        }
    case 20:
        {
            console.log(given_number + 2);
            break;
        }
    case 30:
        {
            console.log(given_number + 3);
            break;
        }
    case 40:
        {
            console.log(given_number + 4);
            break;
        }
    default:
        console.log(given_number + 10);

}