var number=69;

switch(number){
    case 10:{
        console.log(number+1);
        break;
    }
    case 20:{
        console.log(number+2);
        break;
    }
    case 30:{
        console.log(number+3);
        break;
    }
    case 40:{
        console.log(number+4);
        break;
    }
    default:{
        console.log(number+10);
        break;
    }
}
